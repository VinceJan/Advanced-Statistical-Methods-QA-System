def _seed_concepts(db: Session) -> None:
    for item in CONCEPTS:
        db.add(
            Concept(
                slug=item["slug"],
                name_cn=item["name_cn"],
                name_en=item["name_en"],
                aliases_json=dumps(item["aliases"]),
                chapter=item["chapter"],
                description=item["description"],
            )
        )
    db.commit()


def _seed_edges(db: Session) -> None:
    concepts = {c.slug: c for c in db.query(Concept).all()}
    edge_keys: OrderedDict[tuple[str, str, str], str] = OrderedDict()
    for item in CONCEPTS:
        slug = item["slug"]
        for parent in item["parents"]:
            if parent in concepts:
                edge_keys[(parent, slug, "包含")] = f"{concepts[parent].name_cn} 包含或统摄 {item['name_cn']}。"
                edge_keys[(slug, parent, "属于")] = f"{item['name_cn']} 是 {concepts[parent].name_cn} 下的核心知识点。"
        for related in item["related"]:
            if related in concepts:
                edge_keys[(slug, related, "相关")] = f"{item['name_cn']} 与 {concepts[related].name_cn} 在课程知识体系中相关。"
    for qa in RELATION_QAS:
        edge_keys[(qa["a"], qa["b"], qa["relation"])] = qa["answer"]
        edge_keys[(qa["b"], qa["a"], qa["relation"])] = qa["answer"]
    for (source, target, relation), evidence in edge_keys.items():
        if source in concepts and target in concepts and source != target:
            db.add(
                GraphEdge(
                    source_id=concepts[source].id,
                    target_id=concepts[target].id,
                    relation_type=relation,
                    evidence=evidence,
                )
            )
    db.commit()


