def concept_index() -> dict[str, dict[str, Any]]:
    return {item["slug"]: item for item in CONCEPTS}


def seed_core_data(db: Session) -> None:
    if db.query(func.count(Concept.id)).scalar() == 0:
        _seed_concepts(db)
    if db.query(func.count(GraphEdge.id)).scalar() == 0:
        _seed_edges(db)
    if db.query(func.count(QAPair.id)).scalar() == 0:
        _seed_qa_pairs(db)
    db.commit()


