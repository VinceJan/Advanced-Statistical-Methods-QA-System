def _related_names(item: dict[str, Any], by_slug: dict[str, dict[str, Any]]) -> str:
    names = [by_slug[s]["name_cn"] for s in item["related"][:3] if s in by_slug]
    return "、".join(names) if names else "模型评估、知识图谱和教材来源"
